/**
 * Returns a human-readable Russian sector name.
 * Falls back to capitalising the raw value and replacing underscores with spaces.
 */
export function translateSector(raw: string): string {
  const key = raw.toLowerCase().trim();
  if (SECTOR_MAP[key]) return SECTOR_MAP[key];
  // try replacing underscores
  const noUnderscore = key.replace(/_/g, " ");
  if (SECTOR_MAP[noUnderscore]) return SECTOR_MAP[noUnderscore];
  // fallback: replace underscores + capitalise
  return noUnderscore
    .replace(/\b\w/g, (c) => c.toUpperCase());
}
